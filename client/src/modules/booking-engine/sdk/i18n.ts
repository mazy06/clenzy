const translations: Record<string, Dict> = { fr, en, ar };

export interface BookingI18n {
  t: (key: string) => string;
  tArray: (key: string) => string[];
  tObject: (key: string) => Record<string, string>;
  isRTL: boolean;
  lang: string;
}

export function createBookingI18n(lang: string): BookingI18n {
  const dict = translations[lang] || translations.fr;
  const fallback = translations.fr;
  const isRTL = lang === 'ar';

  return {
    t: (key: string): string => {
      const val = dict[key] ?? fallback[key];
      return typeof val === 'string' ? val : key;
    },
    tArray: (key: string): string[] => {
      const val = dict[key] ?? fallback[key];
      return Array.isArray(val) ? val : [];
    },
    tObject: (key: string): Record<string, string> => {
      const val = dict[key] ?? fallback[key];
      return (val && typeof val === 'object' && !Array.isArray(val)) ? val as Record<string, string> : {};
    },
    isRTL,
    lang,
  };
}
